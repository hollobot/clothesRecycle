-- 校园二手衣物回收系统核心表结构（MySQL 8）
SET NAMES utf8mb4;
CREATE DATABASE IF NOT EXISTS `clothes_recycle`
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_unicode_ci;

USE clothes_recycle;
CREATE TABLE IF NOT EXISTS campus (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  name VARCHAR(64) NOT NULL COMMENT '校区名称',
  address VARCHAR(255) DEFAULT '' COMMENT '校区地址',
  status TINYINT NOT NULL DEFAULT 1 COMMENT '状态：1启用，0禁用',
  remark VARCHAR(255) DEFAULT '' COMMENT '备注',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  UNIQUE KEY uk_campus_name (name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='校区表';

CREATE TABLE IF NOT EXISTS admin (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  phone VARCHAR(32) NOT NULL COMMENT '管理员手机号',
  password VARCHAR(128) NOT NULL COMMENT '管理员密码',
  name VARCHAR(64) NOT NULL COMMENT '管理员姓名',
  role VARCHAR(32) NOT NULL COMMENT '管理员角色：SUPER_ADMIN/CAMPUS_ADMIN',
  campus_id BIGINT DEFAULT NULL COMMENT '所属校区ID，超级管理员可为空',
  status TINYINT NOT NULL DEFAULT 1 COMMENT '状态：1启用，0禁用',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  UNIQUE KEY uk_admin_phone (phone)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='管理员表';

CREATE TABLE IF NOT EXISTS user (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  phone VARCHAR(32) NOT NULL COMMENT '用户手机号',
  password VARCHAR(128) NOT NULL COMMENT '用户密码',
  student_id VARCHAR(64) NOT NULL COMMENT '学号',
  name VARCHAR(64) NOT NULL COMMENT '姓名',
  campus_id BIGINT NOT NULL COMMENT '所属校区ID',
  avatar_url VARCHAR(255) DEFAULT '' COMMENT '头像URL',
  point_balance INT NOT NULL DEFAULT 0 COMMENT '可用积分余额',
  frozen_point INT NOT NULL DEFAULT 0 COMMENT '冻结积分',
  status TINYINT NOT NULL DEFAULT 1 COMMENT '状态：1正常，0禁用',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  UNIQUE KEY uk_user_phone (phone),
  UNIQUE KEY uk_user_student_id (student_id),
  KEY idx_user_campus_id (campus_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='学生用户表';

CREATE TABLE IF NOT EXISTS item (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  user_id BIGINT NOT NULL COMMENT '发布用户ID',
  campus_id BIGINT NOT NULL COMMENT '所属校区ID',
  title VARCHAR(64) NOT NULL COMMENT '物品标题',
  category VARCHAR(32) NOT NULL COMMENT '物品品类',
  gender_type VARCHAR(16) NOT NULL COMMENT '性别类型：男/女/通用',
  size_type VARCHAR(16) NOT NULL COMMENT '尺码类型',
  condition_level VARCHAR(32) NOT NULL COMMENT '新旧程度',
  description VARCHAR(500) NOT NULL COMMENT '物品描述',
  cover_url VARCHAR(255) DEFAULT '' COMMENT '封面图URL',
  acquire_type VARCHAR(16) NOT NULL COMMENT '获取方式：FREE/POINT',
  point_price INT NOT NULL DEFAULT 0 COMMENT '积分价格',
  status VARCHAR(32) NOT NULL COMMENT '物品状态',
  audit_reason VARCHAR(255) DEFAULT '' COMMENT '审核原因或下架原因',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  KEY idx_item_user_id (user_id),
  KEY idx_item_campus_status (campus_id, status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='物品表';

CREATE TABLE IF NOT EXISTS item_image (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  item_id BIGINT NOT NULL COMMENT '物品ID',
  image_url VARCHAR(255) NOT NULL COMMENT '图片URL',
  sort_no INT NOT NULL DEFAULT 0 COMMENT '排序序号',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  KEY idx_item_image_item_id (item_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='物品图片表';

CREATE TABLE IF NOT EXISTS `order` (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  item_id BIGINT NOT NULL COMMENT '物品ID',
  buyer_id BIGINT NOT NULL COMMENT '领取方用户ID',
  seller_id BIGINT NOT NULL COMMENT '发布方用户ID',
  campus_id BIGINT NOT NULL COMMENT '所属校区ID',
  acquire_type VARCHAR(16) NOT NULL COMMENT '获取方式：FREE/POINT',
  point_amount INT NOT NULL DEFAULT 0 COMMENT '本单积分数量',
  status VARCHAR(32) NOT NULL COMMENT '订单状态',
  remark VARCHAR(255) DEFAULT '' COMMENT '订单备注',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  KEY idx_order_item_id (item_id),
  KEY idx_order_buyer_id (buyer_id),
  KEY idx_order_seller_id (seller_id),
  KEY idx_order_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='认领订单表';

CREATE TABLE IF NOT EXISTS point_record (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  change_amount INT NOT NULL COMMENT '积分变动值，正数增加，负数减少',
  biz_type VARCHAR(32) NOT NULL COMMENT '业务类型',
  biz_id BIGINT DEFAULT NULL COMMENT '业务ID',
  remark VARCHAR(255) DEFAULT '' COMMENT '备注说明',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  KEY idx_point_record_user_time (user_id, create_time)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='积分流水表';

CREATE TABLE IF NOT EXISTS message (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  user_id BIGINT NOT NULL COMMENT '接收用户ID',
  title VARCHAR(128) NOT NULL COMMENT '消息标题',
  content VARCHAR(500) NOT NULL COMMENT '消息内容',
  msg_type VARCHAR(32) NOT NULL COMMENT '消息类型',
  read_status TINYINT NOT NULL DEFAULT 0 COMMENT '已读状态：0未读，1已读',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  KEY idx_message_user_read (user_id, read_status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='站内消息表';

CREATE TABLE IF NOT EXISTS sign_record (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  sign_date DATE NOT NULL COMMENT '签到日期',
  reward_point INT NOT NULL DEFAULT 0 COMMENT '签到奖励积分',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  UNIQUE KEY uk_sign_user_date (user_id, sign_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='签到记录表';

CREATE TABLE IF NOT EXISTS drop_point (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  campus_id BIGINT NOT NULL COMMENT '所属校区ID',
  name VARCHAR(64) NOT NULL COMMENT '回收点名称',
  location_desc VARCHAR(255) NOT NULL DEFAULT '' COMMENT '位置描述',
  open_time VARCHAR(64) NOT NULL DEFAULT '' COMMENT '开放时间',
  manager_note VARCHAR(255) NOT NULL DEFAULT '' COMMENT '负责人说明',
  stock_count INT NOT NULL DEFAULT 0 COMMENT '当前存量',
  status TINYINT NOT NULL DEFAULT 1 COMMENT '状态：1启用，0禁用',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  KEY idx_drop_point_campus_id (campus_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='回收点表';

CREATE TABLE IF NOT EXISTS gift (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  name VARCHAR(64) NOT NULL COMMENT '礼品名称',
  description VARCHAR(255) NOT NULL DEFAULT '' COMMENT '礼品描述',
  image_url VARCHAR(255) NOT NULL DEFAULT '' COMMENT '礼品图片URL',
  point_cost INT NOT NULL COMMENT '所需积分',
  stock INT NOT NULL DEFAULT 0 COMMENT '库存',
  exchanged_count INT NOT NULL DEFAULT 0 COMMENT '已兑换数量',
  status TINYINT NOT NULL DEFAULT 1 COMMENT '状态：1上架，0下架',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='积分礼品表';

CREATE TABLE IF NOT EXISTS gift_exchange (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  gift_id BIGINT NOT NULL COMMENT '礼品ID',
  user_id BIGINT NOT NULL COMMENT '兑换用户ID',
  point_cost INT NOT NULL COMMENT '兑换时积分',
  exchange_code VARCHAR(32) NOT NULL COMMENT '兑换码',
  status VARCHAR(16) NOT NULL DEFAULT 'PENDING' COMMENT '状态：PENDING/VERIFIED/CANCELLED',
  verify_time DATETIME DEFAULT NULL COMMENT '核销时间',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  UNIQUE KEY uk_gift_exchange_code (exchange_code),
  KEY idx_gift_exchange_user_time (user_id, create_time),
  KEY idx_gift_exchange_gift_id (gift_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='礼品兑换记录表';

CREATE TABLE IF NOT EXISTS favorite (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '主键ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  item_id BIGINT NOT NULL COMMENT '物品ID',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT '逻辑删除标记：0未删除，1已删除',
  UNIQUE KEY uk_favorite_user_item (user_id, item_id),
  KEY idx_favorite_item_id (item_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='收藏表';
